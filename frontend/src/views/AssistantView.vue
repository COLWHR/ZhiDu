const categories = computed(() => {
  const allCategories = new Set<string>();
  systemAgents.value.forEach(agent => {
    agent.group.forEach(group => allCategories.add(group));
  });
  
  // 添加"所有"分类
  const categoryList = [{ name: '所有', count: systemAgents.value.length }];
  
  // 添加其他分类
  allCategories.forEach(category => {
    const count = systemAgents.value.filter(agent => agent.group.includes(category)).length;
    categoryList.push({ name: category, count });
  });
  
  return categoryList;
});

// 过滤助手
const filteredAgents = computed(() => {
  let agents = systemAgents.value;
  
  // 按分类过滤
  if (currentCategory.value !== '所有') {
    agents = agents.filter(agent => agent.group.includes(currentCategory.value));
  }
  
  // 按搜索词过滤
  if (searchQuery.value) {
    agents = agents.filter(agent => 
      agent.name.toLowerCase().includes(searchQuery.value.toLowerCase()) ||
      agent.description.toLowerCase().includes(searchQuery.value.toLowerCase())
    );
  }
  
  return agents;
});

